import { formatDate } from '../script/utility/date.js';
import { formatCurrency } from '../script/utility/money.js';
import { Category } from '../data/category.js';

export class Transaction{
    #localStorageKey;
    transaction;
    date;
    description;
    category;
    type;
    amount;

    constructor(localStorageKey){
        this.#localStorageKey = localStorageKey;
        this.transaction = undefined;
        this.#loadFromStorage();
    }

    #loadFromStorage(){
        this.transaction = JSON.parse(localStorage.getItem(this.#localStorageKey)) || [];
    }

    saveToStorage(){
        localStorage.setItem(this.#localStorageKey, JSON.stringify(this.transaction));
    }

    addTransaction(date, description, category, type, amount, event){
        let autoGeneratedId = this.transaction.length === 0 ? 1 : Math.max(...this.transaction.map(tran => tran.id)) + 1;
        const transactionDescripCheck = description.trim().toLowerCase();

        const findDuplicateDescription = this.transaction.find(tran => tran.description.toLowerCase() === transactionDescripCheck);

        if(findDuplicateDescription){
            alert('Error this description already exist');
            event.target.closest('.transaction-form').reset();
            return;
        }

        const newTransactionObj = {
            id: autoGeneratedId,
            date,
            description,
            category,
            type,
            amount
        };
        this.transaction.push(newTransactionObj);
        this.saveToStorage();
        this.isTransactionEmpty();
        this.renderTransaction();
        event.target.closest('.transaction-form').reset();
    }

    editTransaction(transactionId, date, description, category, type, amount){
        const matchingItem = this.transaction.find(transac => transac.id === transactionId);

        if(matchingItem){
            matchingItem.type = type;
            matchingItem.amount = amount;
            matchingItem.date = date;
            matchingItem.category = category; 
            matchingItem.description = description; 
        }

        this.saveToStorage();
        this.isTransactionEmpty();
        this.renderTransaction();
    }

    deleteTransaction(transactionId){
        this.transaction = this.transaction.filter(transac => transac.id !== transactionId);
        const tableRow = document.querySelector(`.row-${transactionId}`);
        if(tableRow) tableRow.remove();
        this.saveToStorage();
        this.isTransactionEmpty();
        this.renderTransaction();
    }

    openEditModal(transactionId){
        const editTransacModalContent = document.querySelector('.edit-transaction-modal');
        const transactionItem = this.transaction.find(transac => transac.id === transactionId);

        const innerModalHTML = 
        `
        <div class="edit-transaction-header">
            <h3>Edit Transaction</h3>
            <div class="back-btn-transaction-container" id="close-edit-transaction-modal">
                <img src="svg/x.svg" alt="X button">
            </div>
        </div>

        <form class="transaction-form" id="edit-transaction-form">
            <div class="info-container">
                <h4>Category</h4>
                <div class="form-group-name">
                    <select name="category-type" class="custom-select" id="category-edit-transaction" title="Category type">
                    </select>
                </div>
            </div>
            <div class="info-container">
                <h4>Amount</h4>
                <div class="form-group-name">
                    <div class="pesos-container">
                        <h4>₱</h4>
                    </div>
                    <input class="income-text" type="text" id="incomeEditText" value="${transactionItem.amount}"  placeholder="Enter amount" name="income_text">
                </div>
            </div>
            <div class="info-container">
                <h4>Date</h4>
                <div class="form-group-name">
                    <input class="date" type="date" value="${transactionItem.date}" id="transaction-edit-date">
                </div>
            </div>
            <div class="info-container">
                <h4>Description </h4>
                <div class="form-group-name">
                    <textarea id="edit-description"  name="description" placeholder="Enter description..." maxlength="150">${transactionItem.description}</textarea>
                </div>
            </div>
            <div class="form-btn-container">
                <button class="cancel-transaction js-cancel-transaction" id="cancel-edit-transaction">Cancel</button>
                <button class="save-transaction js-save-transaction" id="save-edit-transaction">Save Changes</button>
            </div>
        </form>
        `;
        if(editTransacModalContent) editTransacModalContent.innerHTML = innerModalHTML;
        this.renderEditTransactionCategory(transactionId);
    }

    isTransactionEmpty(){
        const addTransactionBtn = document.querySelector('.add-transaction-btn');
        const emptyTransactionContainer = document.querySelector('.empty-transaction');
        const tableContainer = document.getElementById('transaction-table');

        if(!addTransactionBtn || !emptyTransactionContainer || !tableContainer) return;

        addTransactionBtn.style.display = this.transaction.length === 0 ? 'none' : 'flex';
        emptyTransactionContainer.style.display = this.transaction.length === 0 ? 'flex' : 'none';
        tableContainer.style.display = this.transaction.length === 0 ? 'none' : 'table';
    }

    renderEditTransactionCategory(transactionId){
        const saveUser = JSON.parse(localStorage.getItem('currentUser'));
        const userCategories = saveUser ? new Category(`categories-${saveUser.id}`) : (window.location.href = 'login.html');

        const transactionItem = this.transaction.find(transac => transac.id === transactionId);

        const categoryTransaction = document.getElementById('category-edit-transaction');
        const defaultOption = `<option value="${transactionItem.category}" data-type-category="${transactionItem.type}">${transactionItem.category}</option>`;
        const optionsHTML = userCategories.category
            .filter(item => item.categoryName)
            .map(({ categoryName, type }) => `
                <option value="${categoryName}" data-type-category="${type}">${categoryName}</option>
            `).join('');
        categoryTransaction.innerHTML = defaultOption + optionsHTML;
    }

    renderTransaction(){
        let transactionHTML = '';
        const tableBody = document.querySelector('.transaction-items');
        this.transaction.forEach(transactionnItem =>{
            const {id, date, description, category, type, amount} = transactionnItem;
            
            const colorClass = type === 'Income' ? 'text-green' : 'text-red';
            const priceSign = type === 'Income' ? '+' : '-';

            if(!date || !description || !category || !type || !amount) return;

            transactionHTML +=
            `
            <tr class="row-${id}" data-category-id="${id}">
                <td>${formatDate(date)}</td>
                <td>${description}</td>
                <td>${category}</td>
                <td>${type}</td>
                <td class="money-type ${colorClass}">${priceSign}₱${formatCurrency(amount)}</td>
                <td class="action-btn-body">
                    <div class="action-btn">
                        <button class="edit-btn js-edit-btn"
                        data-category-id="${id}">
                            <img src="svg/edit.svg" alt="Edit Icon">
                        </button>
                        <button class="delete-btn js-delete-btn"
                        data-category-id="${id}">
                            <img src="svg/delete.svg" alt="Delete Icon">
                        </button>
                    </div>
                </td>
            </tr>
            `;
        });
        tableBody.innerHTML = transactionHTML;
    }
}