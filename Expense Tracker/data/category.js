export class Category{
    #localStorageKey;
    category;
    categoryName;
    type;

    constructor(localStorageKey){
        this.#localStorageKey = localStorageKey;
        this.category = undefined;
        this.#loadFromStorage();
    }

    #loadFromStorage(){
        this.category = JSON.parse(localStorage.getItem(this.#localStorageKey)) || [];
    }

    saveToStorage(){
        localStorage.setItem(this.#localStorageKey, JSON.stringify(this.category));
    }

    isCategoryEmpty(){
        const emptyContainer = document.querySelector('.empty-category');
        const tableContainer = document.querySelector('.table');
        const addCategoryBtn = document.querySelector('.add-category-btn');

        if(!emptyContainer || !tableContainer || !addCategoryBtn) return;

        emptyContainer.style.display = this.category.length === 0 ? 'flex' : 'none';
        tableContainer.style.display = this.category.length === 0 ? 'none' : 'table';
        addCategoryBtn.style.display = this.category.length === 0 ? 'none' : 'flex';
    }

    addCategory(categoryName, type, event){
        let autoGeneratedId = this.category.length === 0 ? 1 : Math.max(...this.category.map(cat => cat.id)) + 1;
        const categoryNameCheck = categoryName.trim().toLowerCase();

        const findDuplicateCategory = this.category.find(cat => cat.categoryName.toLowerCase() === categoryNameCheck);
        
        if(findDuplicateCategory){
            alert('Error this category already exist');
            event.target.closest('.category-form').reset();
            return;
        }

        const newCategoryObj = {
            id: autoGeneratedId,
            categoryName,
            type
        };
        this.category.push(newCategoryObj);
        this.saveToStorage();
        this.isCategoryEmpty();
        this.renderCategory();
        this.renderTransactionCategory();

        autoGeneratedId++;
        // reset the form after adding a category
        event.target.closest('.category-form').reset();
    }

    editCategory(id, categoryName, type, event){
        let matchingItem;
        this.category.forEach(categoryItem =>{
            if(categoryItem.id === Number(id)){
                matchingItem = categoryItem;
            }
        });

        const categoryNameCheck = categoryName.trim().toLowerCase();
        const findDuplicateCategory = this.category.find(cat => cat.categoryName.toLowerCase() === categoryNameCheck);
        if(findDuplicateCategory){
            alert('Error this category already exist');
            event.target.closest('.category-edit-form').reset();
            return;
        }

        matchingItem.categoryName = categoryName;
        matchingItem.type = type;
        this.saveToStorage();
        this.renderCategory();
        this.renderTransactionCategory();
    }

    deleteCategory(categoryId){
        let newArr = [];
        this.category.forEach(categoryItem =>{
            if(categoryItem.id !== Number(categoryId)){
                newArr.push(categoryItem);
            }
        });
        this.category = newArr;
        this.saveToStorage();
        this.isCategoryEmpty();
        this.renderCategory();
        this.renderTransactionCategory();
    }

    openEditModal(id){
        const editCategoryModalContent = document.querySelector('.edit-category-modal');
        const categoryItem = this.category.find(cat => cat.id === id);
        const innnerModalHTML = 
        `
        <div class="edit-category-header">
            <h3>Edit Category</h3>
            <div class="back-btn-container" id="close-edit-categ-modal">
                <img src="svg/x.svg" alt="X button">
            </div>
        </div>
        <form class="category-edit-form" id="edit-category-from">
            <div class="info-container">
                <h4>Category name</h4>
                <div class="form-group-name">
                    <input class="category-text" type="text" id="categoryEditName" value="${categoryItem.categoryName}" required>
                </div>
            </div>
            <div class="info-container">
                <h4>Type</h4>
                <div class="form-group-name">
                    <select name="category-type" class="custom-edit-select" id="typeEditCategory" title="Category type">
                        <option value="" disabled selected hidden>Select category type</option>
                        <option value="Expense" ${categoryItem.type === 'Expense' ? 'selected' : ''}>Expense</option>
                        <option value="Income" ${categoryItem.type === 'Income' ? 'selected' : ''}>Income</option>
                    </select>
                </div>
            </div>
            <div class="form-btn-container">
                <button class="cancel-category js-cancel-edit-category" id="cancel-edit-category">Cancel</button>
                <button class="save-category js-save-edit-category" id="save-changes">Save Changes</button>
            </div>
        </form>
        `;
        if(editCategoryModalContent){
            editCategoryModalContent.innerHTML = innnerModalHTML;
        }
    }

    renderTransactionCategory(){
        let transactionCategoryHTML = '<option value="" disabled selected hidden>Select category</option>';
        const categoryTransaction = document.getElementById('category-transaction');
        this.category.forEach(categoryItem =>{
            const { categoryName }  = categoryItem;

            if(!categoryName) return;
            transactionCategoryHTML +=
            `
                <option value="${categoryName}">${categoryName}</option>
            `;
        });
        categoryTransaction.innerHTML = transactionCategoryHTML;
    }

    renderCategory(){
        let categoryItemsHTML = '';
        const categoryTableBody = document.querySelector('.category-items');
        this.category.forEach(categoryItem =>{
            const {id, categoryName, type} = categoryItem;

            if(!categoryName && !type) return;
            categoryItemsHTML +=
            `
            <tr class="row-${id}" data-category-id="${id}">
                <td>${categoryName}</td>
                <td>${type}</td>
                <td class="action-btn-body">
                    <div class="action-btn">
                        <button class="edit-btn js-edit-btn"
                        data-category-id="${id}">
                            <img src="svg/edit.svg" alt="Edit Icon">
                        </button>
                        <button class="delete-btn js-delete-btn"
                        data-category-id="${id}">
                            <img src="svg/delete.svg" alt="Delete Icon">
                        </button>
                    </div>
                </td>
            </tr>
            `;
        });
        categoryTableBody.innerHTML = categoryItemsHTML;
    }
}